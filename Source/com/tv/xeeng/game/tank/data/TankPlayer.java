package com.tv.xeeng.game.tank.data;

import com.tv.xeeng.game.data.SimplePlayer;

/**
 *
 * @author tuanda
 */
public class TankPlayer extends SimplePlayer {
    
    public int point = 0;
    
    public TankPlayer(long uid) {
        this.isStop = false;
        this.id = uid;
        this.isGiveUp = false;
    }
    
    public void reset() {
        this.isStop = false;
        this.isReady = false;
        this.isWin = false;
        this.isGiveUp = false;
    }
}
