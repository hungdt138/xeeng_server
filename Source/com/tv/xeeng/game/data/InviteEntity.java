package com.tv.xeeng.game.data;

/**
 *
 * @author tuandavrus
 */
public class InviteEntity {
    private int gameId;
    private int inviteId;
    private long betMoney;
    private long matchId; // Added by ThangTD
    
    public InviteEntity(int gameId, int inviteId, long betMoney, long matchId)
    {
        this.gameId = gameId;
        this.inviteId = inviteId;
        this.betMoney = betMoney;
        this.matchId = matchId;
    }

    /**
     * @return the gameId
     */
    public int getGameId() {
        return gameId;
    }

    /**
     * @return the inviteId
     */
    public int getInviteId() {
        return inviteId;
    }

    /**
     * @return the betMoney
     */
    public long getBetMoney() {
        return betMoney;
    }
}
