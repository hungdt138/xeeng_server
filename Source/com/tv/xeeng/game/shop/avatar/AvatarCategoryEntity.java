package com.tv.xeeng.game.shop.avatar;

import com.tv.xeeng.game.data.AIOConstants;

/**
 *
 * @author tuanda
 */
public class AvatarCategoryEntity {

    private int id;
    private String name;

    public AvatarCategoryEntity(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return id + AIOConstants.SEPERATOR_BYTE_1 + name;
    }
}
