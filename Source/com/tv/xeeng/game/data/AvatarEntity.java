package com.tv.xeeng.game.data;

public class AvatarEntity {
	public int id;
	public String description;
	public long money;
	public AvatarEntity() {
		
	}
	public AvatarEntity(int id, String d, long money) {
		this.id = id;
		this.description = d;
		this.money = money;
	}
	public String getDescription() {
		return description;
	}
	public int getId() {
		return id;
	}
}
